package orientacion_objeto;

public class ParticipanteJuegoDeMesa {

	String nick;
	String nombre;
	int edad;
	int puntuacion;
	
	public ParticipanteJuegoDeMesa(String nick, String nombre, int edad, int puntuacion) {
		super();
		this.nick = nick;
		this.nombre = nombre;
		this.edad = edad;
		this.puntuacion = puntuacion;
	}
	
	
}
